    reference_time = np.asarray(finest["figure6_comparison"]["time_fs"])
    reference = np.asarray(finest["figure6_comparison"]["digitized"])
    width, height = 1000, 590
    left, right, top, bottom = 95, 960, 105, 500

    def xmap(value): return left + value / 40.0 * (right - left)
